     That's it!!!!</p><br>

<p style="text-indent: 0px;"><span id="yellowFont">EXAMPLES:</span></p>
  
<p>You are looking for something in <span id="yellowFont">WILDLIFE</span> in Green. You simply scroll down the page to the 
     Design Category heading WILDLIFE. Once there scroll down to the GREEN Color Family and there will be all the 
     Wildlife designs I have in Green beginning with the lightest shade of green to the darkest shade of green.</p><br>  

<p>If you are looking for something in a <span id="yellowFont">SOLID</span> color in Blue, just scroll down the page to the 
     Design Category heading SOLIDS. Once there scroll down to the BLUE Color Family and there will be all the Solids 
     I have in Blue beginning with the lightest shade of blue to the darkest shade of blue.</p><br>

<p>If you are looking for something that is neither Wildlife or Solids, you would go to the Design Category heading 
     "<span id="yellowFont">PRINTS</span>".  Once there scroll down to the Color Family you are looking for, say REDS, 
     and there will be all the Prints I have in Red beginning with the lightest shade of red to the darkest shade of red.</p><br>

<p>Swatches will give you information on their status. If you see a swatch tagged "<span id="yellowFont">SOLD OUT</span>", 
     this means that I am out of this fabric but in the process of trying to get more in. Check back regularly to see if the 
     "SOLD OUT" tag is gone. If it is I have more in. Another status tag is "<span id="yellowFont">DISCONTINUED</span>". 
     If the fabric has been discontinued by the manufacture, I cannot get anymore. Another tag is 
     "<span id="yellowFont">CLEARANCE</span>", which usually gets you a 10-15% discount.</p><br>

