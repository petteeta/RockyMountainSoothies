<p>There is nothing more frustrating to go to a fabric website looking for a certain fabric and have to search through 
     hundreds of bolts of fabric trying to find it.  None of that here!!!</p><br>
     
<p>To make your fabric search easier, I have grouped my flannels by DESIGN . There are only 3, 
     <span id="yellowFont">PRINTS, SOLIDS,</span> and <span id="yellowFont">WILDLIFE.</span> To simplify even further, each of 
